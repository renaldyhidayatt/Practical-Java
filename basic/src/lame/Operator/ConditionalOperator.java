package lame.Operator;

public class ConditionalOperator {
    public static void kentang() {
        int a, b;
        a = 20;
        b = (a == 1) ? 30 : 70;
        System.out.println("Value of b is : " + b);

        b = (a == 40) ? 20 : 50;
        System.out.println("Value of b is : " + b);
    }
}
