package strings;

public class StringVariabel1 {
  public static void kentang() {
    String myName = "William";
    // data type is numeric long, variable name is myLong, value is one
    // million
    long myLong = 1000000;
    // data type is character, variable name is letter, value is C
    char letter = 'C';
    // data type is integer, variable name is myAge, value is 10
    int myAge = 10;

    System.out.println(myName + " " + myLong + " " + letter + " " + myAge);

  }
}
